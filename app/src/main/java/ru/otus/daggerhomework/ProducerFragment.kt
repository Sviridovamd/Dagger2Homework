package ru.otus.daggerhomework

import android.graphics.Color
import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import android.widget.Button
import androidx.fragment.app.Fragment

class ProducerFragment : Fragment() {

    private val eventObserver: EventObserver by lazy {
        (requireActivity() as MainActivity).mainActivityComponent.eventObserver()
    }

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View? {
        return inflater.inflate(R.layout.fragment_producer, container, true)
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        view.findViewById<Button>(R.id.button).setOnClickListener {
            //отправить результат через flow в другой фрагмент
            eventObserver.sendEvent(Event.ChangeColor(Color.RED))
        }
    }
}
